const clientContributionType = require('../../db_apis/hcp/clientContributionType.js');

async function list(req, res, next) {
	console.log('controllers:clientContributionType.list(req, res, next)');
	try {
		const rows = await clientContributionType.list(req.user, req.query);
		res.status(200).json(rows);
	} catch (err) {
		console.error(err.stack);
		next(err);
	}
}
module.exports.list = list;

async function get(req, res, next) {
	console.log('controllers:clientContributionType.get(req, res, next)');
	try {
		const id = parseInt(req.params.id);

		const rows = await clientContributionType.get(req.user, id);
		if (rows.length === 1) {
			res.status(200).json(rows[0]);
		} else {
			res.status(404).end('200');
		}
	} catch (err) {
		console.error(err.stack);
		next(err);
	}
}
module.exports.get = get;

